package Strings;

public class Pattern1 {
     public static void main(String args[]) {
    	 for(int n=3; n>0; n--) {
    		 if(n%2!=0) {
    		  for(int j=1; j<=n; j++) {
    		     System.out.print("@");
    		     System.out.print(" ");
    		 }
    		 
    	 }
    	 System.out.println("");
     }
}
}
